    public void listTab(){
        DefaultTableModel modTab = (DefaultTableModel) tbCac.getModel();
        
        int posLin = 0;
        modTab.setRowCount(posLin);
        
        for(Cacto c1 : oprCac.getBdCac()){
            modTab.insertRow(posLin, new Object[]{c1.getNome(), c1.getGrupoPlanta(), c1.getTamanho(), c1.getQuantEspinhos(), 
                c1.getToleranciaSeca(), c1.getAmbiente().getIluminacao(), c1.getAmbiente().getTemperatura(), c1.getAmbiente().getLocalizacao().getNomeComodo(), c1.getAmbiente().getLocalizacao().getAndarComodo(),});
            posLin++;
        }
    }
    
